// Merge blog translations with main translations
(function () {
  function mergeAndApply() {
    // Wait for window.translations to be available from script.js
    if (typeof window.translations === "undefined") {
      // Wait a bit and try again
      setTimeout(mergeAndApply, 50);
      return;
    }

    // Merge blog translations with main translations
    Object.keys(blogMoiBongTranslations).forEach((lang) => {
      if (window.translations[lang]) {
        window.translations[lang] = {
          ...window.translations[lang],
          ...blogMoiBongTranslations[lang],
        };
      } else {
        window.translations[lang] = blogMoiBongTranslations[lang];
      }
    });

    // Re-apply translations if language switcher is already initialized
    if (
      typeof window.currentLanguage !== "undefined" &&
      typeof window.setLanguage === "function"
    ) {
      // Use a small delay to ensure DOM is ready
      setTimeout(() => {
        window.setLanguage(window.currentLanguage);
      }, 100);
    }
  }

  // Start merging immediately
  mergeAndApply();
})();
